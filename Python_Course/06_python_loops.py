# Задача 12
# Создай список numbers с элементами 1, 2, 3, 4, 5.
# С помощью цикла for выведи квадраты только чётных чисел из списка.
numbers = [1, 2, 3, 4, 5]
for number in numbers:
    if number % 2 == 0:
        print(number ** 2)

