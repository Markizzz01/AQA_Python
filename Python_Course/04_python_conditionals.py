
# Задачи по условным операторам

# Задача 1
# Создай переменную age со значением 18.
# Если возраст >= 18 — выведи "Совершеннолетний", иначе "Несовершеннолетний"
age = 18
if age >= 18:
    print("Совершеннолетний")
else:
    print("Несовершеннолетний")

# Задача 2
# Создай переменную number = 10. Выведи "Чётное", если число делится на 2, иначе "Нечётное"
number = 10
if number % 2 == 0:
    print("Чётное")
else:
    print("Нечётное")

# Задача 3
# Создай переменную temp = 30. Выведи "Холодно", если temp < 0, "Прохладно", если 0 <= temp <= 20, иначе "Тепло"
temp = 30
if temp < 0:
    print("Холодно")
elif temp <= 20:
    print("Прохладно")
else:
    print("Тепло")

