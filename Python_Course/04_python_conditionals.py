# Задача 7
# Создай переменную temperature = 15. С помощью if-elif-else выведи:
# "Жарко", если > 25, "Тепло", если 15-25 включительно, "Холодно" если < 15
temperature = 15
if temperature > 25:
    print("Жарко")
elif 15 <= temperature <= 25:
    print("Тепло")
else:
    print("Холодно")

# Задача 8
# Создай переменную grade = 4. С помощью тернарного оператора присвой переменной mark:
# "Отлично" если 5, "Хорошо" если 4, иначе "Удовлетворительно"
grade = 4
mark = "Отлично" if grade == 5 else "Хорошо" if grade == 4 else "Удовлетворительно"
print(mark)

