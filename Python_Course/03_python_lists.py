
# Задачи по спискам

# Задача 1
# Дан список fruits = ["яблоко", "банан", "вишня"]. Добавь элемент "апельсин" и удали первый элемент.
fruits = ["яблоко", "банан", "вишня"]
fruits.append("апельсин")
print(fruits)  # Вывод ['яблоко', 'банан', 'вишня', 'апельсин']
fruits.pop(0)
print(fruits)  # Вывод ['банан', 'вишня', 'апельсин']

# Задача 2
# Создай список numbers = [10, 20, 30, 40, 50] и выведи элементы с 1-го по 3-й включительно
numbers = [10, 20, 30, 40, 50]
new_numbers = numbers[1:4]
print(new_numbers)  # Вывод [20, 30, 40]

# Задача 3
# Дан список colors = ["red", "green", "blue"]. Замени второй элемент на "yellow"
colors = ["red", "green", "blue"]
colors[1] = "yellow"
print(colors)  # Вывод ['red', 'yellow', 'blue']

# Задача 4
# Создай список fruits = ["яблоко", "груша"] и vegetables = ["морковь", "огурец"]. Объедини их в products
fruits = ["яблоко", "груша"]
vegetables = ["морковь", "огурец"]
products = fruits + vegetables
print(products)  # Вывод ['яблоко', 'груша', 'морковь', 'огурец']

