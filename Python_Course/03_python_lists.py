# Задача 7
# Создай список fruits = ["яблоко", "банан", "киви", "апельсин"]. Выведи каждый элемент с помощью цикла for
fruits = ["яблоко", "банан", "киви", "апельсин"]
for fruit in fruits:
    print(fruit)

# Задача 8
# Создай список numbers = [3, 7, 2, 9]. Выведи каждый элемент, умноженный на 2
numbers = [3, 7, 3, 9]
for number in numbers:
    print(number * 2)

# Задача 9
# Создай список numbers = [1, 2, 3, 4, 5]. Выведи квадраты только чётных чисел
numbers = [1, 2, 3, 4, 5]
for number in numbers:
    if number % 2 == 0:
        print(number ** 2)

# Задача 10
# Создай список names = ["Анна", "Иван", "Мария", "Пётр"]. Выведи имена, пропуская "Мария"
names = ["Анна", "Иван", "Мария", "Пётр"]
for name in names:
    if name == "Мария":
        continue
    print(name)

# Задача 11
# Дан список food = ["хлеб", "сыр", "молоко", "вода"]. Останови цикл при встрече "молоко"
food = ["хлеб", "сыр", "молоко", "вода"]
for item in food:
    if item == "молоко":
        break
    print(item)